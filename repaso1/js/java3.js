'use strict'

/* 
    Hacer un programa que muestre todos los números
    entre dos números introducidos por los usuarios

*/

let num1 = parseInt(prompt("Introduce el primer número", 0));
let num2 = parseInt(prompt("Introduce el segundo número", 0));


for( let i = num1; i <= num2; i++) {
    console.log(`Estos son los números que hay entre ${num1} y ${num2}: ${i}`)
}






















